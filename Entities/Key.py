class Key:
    
    # ---------------------------
    # constructor
    # ---------------------------
    def __init__(self, room):
        self.setRoom(room)
        self.available = True

    # ---------------------------
    # convert object to string
    # ---------------------------
    def __str__(self):
        return(str(self.__dict__))
            
    # ---------------------------
    # ---------------------------
    def setRoom(self, room):
        self.room = room
    
    # ---------------------------
    # ---------------------------
    def getRoom(self):
        return(self.room)
        
    # ---------------------------
    # ---------------------------
    def withdrawKey(self):
        self.available = False

    # ---------------------------
    # ---------------------------
    def returnKey(self):
        self.available = True
            
    # ---------------------------
    # ---------------------------
    def isAvailable(self):
        return(self.available)
        
